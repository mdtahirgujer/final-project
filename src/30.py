def add_numbers(a, b):
    """
    Add two numbers.
    
    Parameters:
        a (float): The first number.
        b (float): The second number.
        
    Returns:
        float: The sum of a and b.
    """
    return a + b

# Example usage
result = add_numbers(3.5, 2.1)
print(result)  # Output: 5.6
